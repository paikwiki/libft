#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (s[i] != c)
	{
		if (!s)
			return (0);
		i++;
	}
	return ((char *)&s[i]);
}
